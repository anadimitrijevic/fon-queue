package rs.fon.queue.model;

public class InformationModel {

	private String status;
	private String nextOrdNumber;
	private String nextStudentIndex;
	private int queueSize;
	
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getNextOrdNumber() {
		return nextOrdNumber;
	}
	public void setNextOrdNumber(String nextOrdNumber) {
		this.nextOrdNumber = nextOrdNumber;
	}
	public String getNextStudentIndex() {
		return nextStudentIndex;
	}
	public void setNextStudentIndex(String nextStudentIndex) {
		this.nextStudentIndex = nextStudentIndex;
	}
	public int getQueueSize() {
		return queueSize;
	}
	public void setQueueSize(int queueSize) {
		this.queueSize = queueSize;
	}
	
	
	
	
	
	
}
